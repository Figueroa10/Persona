public class Persona {
    //Atributos
    private String Nombre;
    private String Apellido;
    private int Edad;
    //Constructor
    
    public Persona (String N,String A,int E){
        this.Nombre = N;
        this.Apellido = A;
        this.Edad = E;
        
    //Metodos
    }
    public String getN() {
        return Nombre;
    }
    public String getA() {
        return Apellido;
    }
    public int getE() {
        return Edad;
    }
    }
    
    
    

